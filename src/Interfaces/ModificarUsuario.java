    private void botonMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonMActionPerformed
         String nombreUser = objM.usuarioExiste(textoU.getText());
        if(!textoU.getText().isEmpty() && !textoC.getText().isEmpty()){
            if(!"0".equals(nombreUser) && !nombreU.equals(nombreUser)){
                objMen.setVisible(true);
                objMen.setMensaje("El nombre de usuario ya existe, elige otro");
                textoU.setText("");
            }
            else{
                objM.modificarUsuario(nombreU, textoC.getText(), textoU.getText());
                objMen.setVisible(true);
                objMen.setMensaje("El usuario se ha introducido correctamente");
                nombreU = textoU.getText();
                password = textoC.getText();
            }
        }
        else{
                objMen.setVisible(true);
                objMen.setMensaje("Introduce ambos campos para continuar");
        }             
    }//GEN-LAST:event_botonMActionPerformed

