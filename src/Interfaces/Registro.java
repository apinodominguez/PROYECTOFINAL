    private void botonAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAActionPerformed
        String nombreU = objM.usuarioExiste(textoU.getText());
        String contraseña = objM.contraseñaExiste(textoC.getText());
        String tipoU = objM.tipoUsuario(nombreU);
        if(textoU.getText().isEmpty() || textoC.getText().isEmpty()){
            objMen.setVisible(true);
            objMen.setMensaje("Debes introducir ambos campos para continuar");
        }
        else{
            if(textoU.getText().equals(nombreU) && textoC.getText().equals(contraseña)){
                if("U".equals(tipoU)){
                    objU.setVisible(true);
                    setVisible(false);
                }
                if("A".equals(tipoU)){
                    objA.setVisible(true);
                    setVisible(false);
                }
                setNombreU(nombreU);
                objU.setVariables(nombreU, contraseña);
            }    
            else{
                objMen.setVisible(true);
                objMen.setMensaje("El usuario o la contraseña son incorrectos");
            }
        }        
    }//GEN-LAST:event_botonAActionPerformed

