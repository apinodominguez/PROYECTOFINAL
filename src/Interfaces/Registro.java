    private void botonRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRActionPerformed
        String nombreU = objM.usuarioExiste(textoU.getText());
        if(!textoU.getText().isEmpty() && !textoC.getText().isEmpty()){
            if(!"0".equals(nombreU)){
                objMen.setVisible(true);
                objMen.setMensaje("El nombre de usuario ya existe, elige otro");
                textoU.setText("");
            }
            else{
                objM.registrarse(textoU.getText(), textoC.getText(),"U");
                objMen.setVisible(true);
                objMen.setMensaje("El usuario se ha introducido correctamente");
            }
        }
        else{
                objMen.setVisible(true);
                objMen.setMensaje("Introduce ambos campos para continuar");
        }
    }//GEN-LAST:event_botonRActionPerformed

    
    
